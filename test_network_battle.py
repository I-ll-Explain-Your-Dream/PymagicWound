# 测试局域网对战功能的简单说明
# 
# 使用方法：
# 1. 运行 main_gui.py
# 2. 创建至少一个牌组（包含3个角色和20张卡牌）
# 3. 点击"局域网联机"
# 4. 一方选择"创建房间"，另一方选择"加入房间"
# 5. 输入玩家名称和端口（默认4000）
# 6. 主机等待连接，客户端输入主机地址并连接
# 7. 连接成功后，主机点击"开始战斗"
# 8. 进行对战

